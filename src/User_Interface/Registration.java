    private void Submit(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit
        String Email=jTextField1.getText(),Password1=jPasswordField1.getText(),Password2=jPasswordField2.getText(),Name=jTextField4.getText(),Role=jTextField12.getText();
        Pattern pattern = Pattern .compile("^([a-zA-Z0-9_\\-\\.]+)@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.)|(([a-zA-Z0-9\\-]+\\.)+))([a-zA-Z]{2,4}|[0-9]{1,3})(\\]?)$");
        Matcher matcher = pattern.matcher(Email);
        if(Email.equals("")||Password1.equals("")||Password2.equals("")||Name.equals("")||(Role.equals("")&&(bg.isSelected(jRadioButton2.getModel())||bg.isSelected(jRadioButton3.getModel()))))
        {
            JOptionPane.showMessageDialog(this,"Please fill in information blank required!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(!matcher.matches())
        {
            JOptionPane.showMessageDialog(this,"Please input correct Email address!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(Password1.length()<6||Password1.length()>20)
        {
            JOptionPane.showMessageDialog(this,"Password should be between 6 and 20!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(!Password1.equals(Password2))
        {
            JOptionPane.showMessageDialog(this,"Passwords input twice should be same!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(new User().CheckUserExistence(Email))
        {
            JOptionPane.showMessageDialog(this,"The Email has already registered!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String arg[];
            ID = "" + (Integer.parseInt(ID)+1);
            if(bg.isSelected(jRadioButton1.getModel()))
            {
                arg=new String[11];
                arg[0]=ID;arg[1]=Email;arg[2]=Name;arg[3]=jTextField5.getText();arg[4]=Password1;arg[5]=jTextField6.getText();arg[6]=jTextField7.getText();arg[7]=jTextField8.getText();arg[8]=jTextField9.getText();arg[9]=jTextField10.getText();arg[10]=jTextField11.getText();
                new Resident().NewResident(arg);
            }
            else if(bg.isSelected(jRadioButton2.getModel()))
            {
                arg=new String[6];
                arg[0]=ID;arg[1]=Email;arg[2]=Name;arg[3]=jTextField5.getText();arg[4]=Password1;arg[5]=Role;
                new Council_Member().NewCouncil_Member(arg);
            }
            else if(bg.isSelected(jRadioButton3.getModel()))
            {
                arg=new String[6];
                arg[0]=ID;arg[1]=Email;arg[2]=Name;arg[3]=jTextField5.getText();arg[4]=Password1;arg[5]=Role;
                new Managing_Agent().NewManaging_Agent(arg);
            }
            updateconfiguration();
            JOptionPane.showMessageDialog(this,"Register successfully!","Congratulations!",JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            Command base = new Command();
            base.setVisible(true);
            base.setLocationRelativeTo(null); 
        }
    }//GEN-LAST:event_Submit

