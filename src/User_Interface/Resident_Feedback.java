    private void Submit(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please input a title!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(jTextArea1.getText().length()<10)
        {
            JOptionPane.showMessageDialog(this,"Content should not be less than 10 words!","Warning!",JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            String arg[] = new String [4];
            ID = "" + (Integer.parseInt(ID)+1);
            SerialNum1 = "" + (Integer.parseInt(SerialNum1)+1);
            arg[0] = ID; arg[1] = "A" + SerialNum1;arg[2] =jTextField1.getText(); arg[3] = jTextArea1.getText();
            new Case().NewCase(ResidentEmail, arg);
            JOptionPane.showMessageDialog(this,"Feedback proposed! Thanks for your participation!","Congratulations!",JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            Resident_UI RUI = new Resident_UI(ResidentEmail);
            RUI.setVisible(true);
            RUI.setLocationRelativeTo(null); 
            updateconfiguration();
        }
    }//GEN-LAST:event_Submit
    public void updateconfiguration()
    {
        try
        {
            fw = new FileWriter("Configuration.ini");
            bw = new BufferedWriter(fw);
            bw.write("[ID]\r\n"+ID+"\r\n[Serial number]\r\n"+SerialNum1+"\r\n"+SerialNum2);
            bw.close();
            fw.close();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
